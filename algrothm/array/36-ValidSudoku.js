/**
 * @param {character[][]} board
 * @return {boolean}
 */
var isValidSudoku = function(board) {
  const rows = []
  const cols = []
  const sub = []
  for (let i = 0; i < board.length; i++) {
    rows.push([])
    cols.push([])
    sub.push([])
  }

  for (let i = 0; i < board.length; i++) {
    for (let j = 0; j < board[i].length; j++) {
      const val = board[i][j]
      if (val === '.') {
        continue
      }
      if (rows[i][val] || cols[j][val] || sub[Math.floor(i / 3) * 3 + Math.floor(j / 3)][val]) {
        return false
      } else {
        rows[i][val] = true
        cols[j][val] = true
        sub[Math.floor(i / 3) * 3 + Math.floor(j / 3)][val] = true
      }
    }
  }
  return true
};

